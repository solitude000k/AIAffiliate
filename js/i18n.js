/* ═══════════════════════════════════════════════
   言語切替エンジン
═══════════════════════════════════════════════ */
let currentLang = localStorage.getItem('kt-lang') || 'ja';

function t(key) {
  const dict = I18N[currentLang] || I18N['ja'];
  return (dict[key] !== undefined) ? dict[key] : (I18N['ja'][key] !== undefined ? I18N['ja'][key] : key);
}

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('kt-lang', lang);
  document.documentElement.lang = lang;

  // テキスト置換
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = t(el.dataset.i18n);
    if (v !== undefined) el.textContent = v;
  });
  // HTML置換
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = t(el.dataset.i18nHtml);
    if (v !== undefined) el.innerHTML = v;
  });
  // placeholder置換
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const v = t(el.dataset.i18nPlaceholder);
    if (v !== undefined) el.placeholder = v;
  });

  // 言語ボタン active
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // カード再レンダリング
  if (typeof renderCards === 'function') {
    renderCards(typeof currentGenre !== 'undefined' ? currentGenre : 'all');
  }

  // フィルタータブ再翻訳
  const genreMap = {'all':'filter.all','炉端焼き':'filter.robata','ラーメン':'filter.ramen',
    '洋食':'filter.yoshoku','そば':'filter.soba','海鮮':'filter.kaisen','ザンギ':'filter.zangi'};
  document.querySelectorAll('.area-tab').forEach(btn => {
    const m = (btn.getAttribute('onclick')||'').match(/filterGenre\('([^']*)'\)/);
    if (m && genreMap[m[1]]) btn.textContent = t(genreMap[m[1]]);
  });
  const mapGenreMap = {'all':'map.all','ラーメン':'map.ramen','洋食':'map.yoshoku','そば':'map.soba'};
  document.querySelectorAll('.map-btn').forEach(btn => {
    const m = (btn.getAttribute('onclick')||'').match(/filterMap\('([^']*)'/);
    if (m && mapGenreMap[m[1]]) btn.textContent = t(mapGenreMap[m[1]]);
  });
}

document.addEventListener('DOMContentLoaded', () => applyLang(currentLang));
